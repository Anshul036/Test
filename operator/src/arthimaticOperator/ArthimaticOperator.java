package arthimaticOperator;

public class ArthimaticOperator 
{

	public static void main(String[] args) 
	{
		int num1 = 23;
		int num2 = 234;
		
		//Addition +
		int ans = num1+num2;								//Addition
		System.out.println(ans);
		
		System.out.println("ans = "+1);						//Concatenation
		
		System.out.println('A'+'B');						//Concatenation
		
		//Subtraction -
		int sub = num1-num2;
		System.out.println("sustraction = "+sub);			//Subtraction
		
		//Multiplication *
		int mul = num1*num2;
		System.out.println("Multiplication = "+ mul );		//Multiplication
		
		//Division /
		int div = num2 / num1;
		System.out.println("Division = "+ div);				//Division
		
		//Modulo %
		int mud = num2%num1;
		System.out.println("Modulo = "+ mud);				//Modulo
		
		
	}

}
